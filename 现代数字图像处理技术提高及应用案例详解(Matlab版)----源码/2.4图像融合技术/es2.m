function Y = es2(X, n)
% 功能：将图像矩阵进行扩展
% 输入：X  - 输入的二维图像矩阵    n  - 要扩展的行数和列数
% 输出：Y  - 扩展后的矩阵
 [z s] = size(X);
Y  = zeros(z+2*n, s+2*n);
Y(n+1:n+z,n:-1:1)        = X(:,2:1:n+1); 
Y(n+1:n+z,n+1:1:n+s)     = X(:,:);
Y(n+1:n+z,n+s+1:1:s+2*n) = X(:,s-1:-1:s-n);
Y(n:-1:1,n+1:s+n)        = X(2:1:n+1,:); 
Y(n+z+1:1:z+2*n,n+1:s+n) = X(z-1:-1:z-n,:); 
